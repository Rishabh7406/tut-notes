# Dictionary Operations

# Create a dictionary of 3 products with their prices
products = {"Laptop": 800, "Phone": 500, "Tablet": 300}

# Increase the price of a specific product
products["Phone"] += 50

# Remove a product
products.pop("Tablet")

# Print the final dictionary
print(products)  # Output: {'Laptop': 800, 'Phone': 550}
